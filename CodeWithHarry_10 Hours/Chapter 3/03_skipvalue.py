mystring = "abcdefghijklmnopqrstuvwxyz"

print(mystring[1:9:2]) # it will print values from index 1 to 9 with skipping 1 character
print(mystring[1:9:3]) # it will skip 2 indexes and print
print(mystring[1:]) # it will print all values from index 1
print (mystring[:5]) # it will print all values upto index 4 (5-1) 